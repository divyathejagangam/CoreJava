package com.edu;

public class TypeVariable {

	public static void main(String[] args) {
		int ival=25;
		char name='a';
		float fval=25.23f;
		double dval=26.77;
		System.out.println("integer value="+ ival);
		System.out.println("character value"+ name);
		System.out.println("floating value="+ fval);
		System.out.println("double value="+ dval);
	}

}
